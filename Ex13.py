#• ถ้ามีความยาวเท่ากันให้พิมพ์ same 
#• ถ้าไม่เท่ากันให้พิมพ์ “not same”

Char1 = len(input("กรอกจำนวนอักขระอันที่1 :"))
Char2 = len(input("กรอกจำนวนอักขระอันที่2 :"))

