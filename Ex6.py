
Number = int(input("กรุณาป้อนตัวเลข : "))

if Number > 0 :
    if Number  % 2 == 0 :
            print(f"{Number} is positive even ")
    else :
        print(f"{Number} is positive odd ")
        
if Number < 0 :
    if Number % 2 == 0 :
        print(f"{Number} is Negative even ")
    else :
        print(f"{Number} is Negative odd ")
        
if Number == 0 :
     print("Zero!!!!!!!!!!") 
    
