#14. เขียนโปรแกรมรับอินพุต 1 ตัวที่เป็นจํานวนเต็ม และตรวจสอบว่าจํานวนที่รับ
#มาหารด้วย 3 ลงตัวหรือไม่ 
#• ถ้าลงตัวให้พิมพ์ “หารด้วย 3 ลงตัว” 
#• แต่ถ้าไม่ลงตัว ให้พิมพ์ “หารด้วย 3 ไม่ลงตัว”

num1 = int(input("กรอกจำนวนที่ต้องการหารด้วย 3 คือ "))

if num1 % 3 == 0 :
    print(f"จำนวน {num1} หารด้วย 3 ลงตัว")
else :
    print(f"จำนวน {num1} หารด้วย 3 ไม่ลงตัว")