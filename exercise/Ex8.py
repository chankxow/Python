#8. เขียนโปรแกรมรับอินพุต 1 ตัว เป็นปีคริสตศักราช (จำนวนเต็ม) และตรวจสอบว่า จำนวนที่รับเข้ามามีค่ามากกว่าหรือเท่ากับ 0หรือไม่
#   • ถ้ามีค่ามากกว่าหรือเท่ากับ 0 ให้คำนวณปีพุทธศักราช (ปีคริสตศักราช+543)
#	• ถ้ามีค่าน้อยกว่า 0 ให้พิมพ์ “กรุณากรอกข้อมูลที่มากกว่าหรือเท่ากับ 0”
#

kristsak = int(input("ปี : "))
puttaasak = kristsak + 543
if kristsak >= 0 :
    print(f"ปีพุทธศักราช นี้คือ :{puttaasak} ")
else :
    print("“กรุณากรอกข้อมูลที่มากกว่าหรือเท่ากับ 0”")