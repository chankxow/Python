#เขียนโปรแกรมรับอินพุต 2 ตัวที่เป็นจํานวนจริง และตรวจสอบว่าจํานวนที่รับมา
#• ถ้าตัวใดมีค่ามากกว่าและพิมพ์จํานวนนั้นออกมา 
#• แต่ถ้าจํานวนทั้งสองมีค่าเท่า กันให้พิมพ์ “มีค่าเท่ากัน” 



num1 = int(input("เลขตัวแรกคือ : "))
num2 = int(input("เลขตัวแรกคือ : "))

if num1 > num2 :
    print(f"จำนวน {num1} มีค่ามากกว่า {num2}")
elif num2 > num1 :
    print(f"จำนวน{num2} มีค่ามากกว่า {num1}")
    
if num1 == num2 :
    print("จำนวนทั้งสองมีค่าเท่ากัน")