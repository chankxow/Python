#15. เขียนโปรแกรมรับอินพุต 1 ตัวที่เป็นจํานวนเต็ม และตรวจสอบว่าจํานวนที่รับ มาหารด้วย 3 และ 5 ลงตัวหรือไม่ 
#• ถ้าลงตัวให้พิมพ์ “หารด้วย 3 และ 5 ลงตัว 
#• แต่ถ้าไม่ลงตัวให้พิมพ์ “หารด้วย 3 และ 5 ไม่ลงตัว’’ 



num1 = int(input("กรอกจำนวนที่ต้องการหารด้วย 3 กับ 5 : "))

if num1 % 3 == 0 and num1 % 5 == 0 :
    print(f"จำนวน {num1} หารด้วย 3 และ 5 ลงตัว")
else:
    print(f"จำนวน {num1} หารด้วย 3 และ 5 ไม่ลงตัว")

