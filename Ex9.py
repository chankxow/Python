#9. เขียนโปรแกรมรับอินพุต 1 ตัว เป็นอุณหภูมิในหน่วยฟาเรนไฮต์ (จำนวนจริง) และตรวจสอบว่า จำนวนที่รับมามีค่ามากกว่าหรือเท่ากับ 32 หรือไม่
#	• ถ้ามีค่ามากกว่าหรือเท่ากับ 32 ให้คำนวณอุณหภูมิในหน่วยองศาเซลเซียสและพิมพ์
#  ค่าออกมา
#		c=(5*(F-32))9
#	• ถ้ามีค่าน้อยกว่า 32 ให้พิมพ์ “Cold”



FHR = int(input("กรอกจำนวนอุณหภูมิ : " ))

C = (5*(FHR-32))/9

if FHR >= 32 :
    print("'อุณหภูมิคือ {C:.2f} องศา")
elif FHR < 32 :
    print(f"อุณหภูมิคือ {C:.2f} องศา")
    print("IS SO COLD!!!!")