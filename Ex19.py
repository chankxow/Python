#19. ถ้าคุณเป็นโปรแกรมเมอร์ที่ห้างสรรพสินค้าแห่งหนึ่ง คุณได้รับมอบหมายให้เขียนโปรแกรมซึ่งมีรายละเอียดดังต่อไปนี้
#	• รับอินพุต 2 ตัว ที่เป็นจำนวนเต็ม (hour,minute) เพื่อคำนวณค่าที่จอดรถ
#• ถ้าผู้ใช้ป้อนตัวเลขติดลบตัวใดตัวหนึ่งหรือ ทั้งสองจำนวน 
#		ให้พิมพ์ “กรุณาป้อนตัวเลขที่ไม่ติดลบ”
#	• โดยชั่วโมงแรกจอดฟรี หลังจากนั้นเสียค่าจอด ชั่วโมงละ 30 บาท
#	• เศษของชั่วโมงนับเป็น 1 ชั่วโมง
